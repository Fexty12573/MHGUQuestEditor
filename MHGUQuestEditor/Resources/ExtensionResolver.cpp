QString Resources::ExtensionResolver::resolve(u32 hash)
{
    const auto it = extensions.find(hash);
    if (it == extensions.end())
    {
        return QString(".%1").arg(hash, 8, 16, QChar('0'));
    }

    return it->second;
}

